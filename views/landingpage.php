$(document).ready(function(){
	const ratio = 1920/600;

	set_banners();
	$(window).on('resize', set_banners);
	set_logo();
	$(window).on('resize', set_logo);
	

	function set_logo(){
		$('#logo').css('padding-top',''); //reset padding. problemen met padding en hoogte berekening
		let value=($('#banner').height()/2)-($('#logo').height()/2)+'px';
		$('#logo').css('padding-top',value);
	}
	
	function set_banners(){
		let value=$('.banner1').width();
		let newHeight=value/ratio;
		$('.banner').css('height', newHeight+'px');
	}

	const slideArr=['3dontwerp','miniforest','keuken','kast','lamp'];
	let countSlide=0;
	switch_slide();
	function switch_slide(){
		$('#'+slideArr[countSlide]+'.slideshow').fadeIn();
		$('#'+slideArr[countSlide]+'.slidetext').fadeIn();
		
		$('#ticker i').each(function(index,val){
			if ($(val).hasClass("fa-circle")) {
				$(val).removeClass("fa-circle");
				$(val).addClass("fa-circle-dot");
			}
		});
		$('#ticker #'+slideArr[countSlide]).removeClass("fa-circle-dot");
		$('#ticker #'+slideArr[countSlide]).addClass("fa-circle");
		
		let lastSlide=countSlide-1;
		if(countSlide<=0){
			lastSlide=slideArr.length - 1;
		}
		$('#'+slideArr[lastSlide]+'.slideshow').fadeOut();
		$('#'+slideArr[lastSlide]+'.slidetext').fadeOut();
		if(countSlide>=(slideArr.length - 1)){
			countSlide=0;
		}else{
			countSlide+=1;
		}
		setTimeout(function(){
			switch_slide();
		},7500);
	}

  
});

